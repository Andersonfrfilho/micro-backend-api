// ─── Seeders ─────────────────────────────────────────────────────────────────

async function seedTable(
  dataSource: DataSource,
  tableName: string,
  buildRow: (record: BenchmarkRecord) => Record<string, unknown>,
): Promise<number> {
  const queryRunner = dataSource.createQueryRunner();
  let inserted = 0;

  for (const record of RECORDS) {
    const existing = await queryRunner.query(
      `SELECT 1 FROM "${tableName}" WHERE email = $1 LIMIT 1`,
      [record.email],
    );

    if (existing.length > 0) {
      console.log(`  [skip] ${tableName}: ${record.email}`);
      continue;
    }

    const row = buildRow(record);
    const columns = Object.keys(row).map((k) => `"${k}"`).join(', ');
    const placeholders = Object.keys(row).map((_, i) => `$${i + 1}`).join(', ');
    const values = Object.values(row);

    await queryRunner.query(
      `INSERT INTO "${tableName}" (${columns}) VALUES (${placeholders})`,
      values,
    );

    console.log(`  [ok]   ${tableName}: ${record.name} (${record.email})`);
    inserted++;
  }

  await queryRunner.release();
  return inserted;
}

export async function seedBenchmarkTables(dataSource: DataSource): Promise<void> {
  console.log('  → benchmark_uuid_v7 (UUID v7 — PostgreSQL uuidv7())');
  // UUID v7 is generated by the DB default, so we omit the id column
  await seedTable(dataSource, 'benchmark_uuid_v7', (r) => ({
    name: r.name,
    email: r.email,
    age: r.age,
    city: r.city,
    data: JSON.stringify(r.data),
  }));

  console.log('  → benchmark_uuid_v4 (UUID v4 — gen_random_uuid())');
  await seedTable(dataSource, 'benchmark_uuid_v4', (r) => ({
    name: r.name,
    email: r.email,
    age: r.age,
    city: r.city,
    data: JSON.stringify(r.data),
  }));

  console.log('  → benchmark_nanoid (NanoID varchar(21))');
  await seedTable(dataSource, 'benchmark_nanoid', (r) => ({
    id: generateNanoid(),
    name: r.name,
    email: r.email,
    age: r.age,
    city: r.city,
    data: JSON.stringify(r.data),
  }));

  console.log('  → benchmark_snowflake (Snowflake bigint)');
  await seedTable(dataSource, 'benchmark_snowflake', (r) => ({
    id: generateSnowflake().toString(),
    name: r.name,
    email: r.email,
    age: r.age,
    city: r.city,
    data: JSON.stringify(r.data),
  }));
}
