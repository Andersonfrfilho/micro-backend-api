// ─── ID Generators ────────────────────────────────────────────────────────────

const NANOID_ALPHABET = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789_-';

function generateNanoid(size = 21): string {
  const bytes = randomBytes(size);
  return Array.from(bytes)
    .map((b) => NANOID_ALPHABET[b % NANOID_ALPHABET.length])
    .join('');
}

let snowflakeSequence = 0;
const SNOWFLAKE_EPOCH = 1_700_000_000_000n; // custom epoch (Nov 2023)

function generateSnowflake(): bigint {
  const timestamp = BigInt(Date.now()) - SNOWFLAKE_EPOCH;
  const machineId = 1n;
  const sequence = BigInt(snowflakeSequence++ % 4096);
  return (timestamp << 22n) | (machineId << 12n) | sequence;
}

